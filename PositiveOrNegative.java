package day1.oct;

public class PositiveOrNegative {

	public static void main(String[] args) {
		int n = -5;int m = -1;
		System.out.println("n=Negative_Number");
		
		if (n<0) {
			int x =n*m;
			System.out.println("Number -5 converted into positive "+x);
			
		}
	
		else {
			System.out.println("Already a positive number");
		}
				

	}

}
